def kelvin_to_celsius(n):
    return n - 273.15

def kelvin_to_fahrenheit(n):
    return (9 / 5) * (n - 273.15) + 32

def celsius_to_fahrenheit(n):
    return (n * 9 / 5) + 32

def celsius_to_kelvin(n):
    return n + 273.15

def fahrenheit_to_celsius(n):
    return (n - 32) / 1.8

def fahrenheit_to_kelvin(n):
    return (5 / 9) * (n - 32) + 273.15